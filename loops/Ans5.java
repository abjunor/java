package language.Java.loops;
public class Ans5 {
    public static void main(String[] args) {
        // 5. Write a C program to print all odd number between 1 to 100.
        int i = 2 ;
        while (1<=i && i<=100) {
            if ((i%2)==1) {
                System.out.println("odd");
            }
            
            i++;
        }
        
    }
}
